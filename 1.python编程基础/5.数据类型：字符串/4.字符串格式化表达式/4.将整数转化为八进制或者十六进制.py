'''格式控制符o表示将整数转化为八进制，x和X表示将蒸熟转化为十六进制'''
print('%o %o'%(123,-123))   #按着默认格式转化为八进制

print('%6o %6o'%(123,-123))     #转化为八进制并制定宽度

print('%06o %06o'%(123,-123))       #指定宽度并填充0

print('%x   %X'%(223,-223))     #按着默认格式转化为十六进制

print('%6x   %6X'%(223,-223))      #指定宽度

print('%06x   %06X'%(223,-223))      #指定宽度并填充0