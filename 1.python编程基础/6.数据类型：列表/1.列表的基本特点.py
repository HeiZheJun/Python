'''python中的数据可以分为四个类型'''
'''
·集合：集合中的元素无序、不可改变
·数字：整数、浮点数、复数、分数等，支持更重算数运算
·序列：字符串、列表和元组都属于序列，支持索引、分片、合并定操作
·映射：字典，将键映射到值
'''

'''列表通常用方括号表示[]例如：[123]。列表对象是一种有序的序列，其主要特点如下：
·   列表可以包含任意类型的对象：数字、字符串、列表、元组或者其他对象
·   列表是一种有序序列，与字符串类似，列表中的每一项按照从左到右的顺序
可以通过为止偏移量进行索引和修改
·   列表是可变的。首先列表长度可变，即可添加或删除列表成员，其次列表中的
对象可以直接修改
·   列表存储的是对象的引用，每个列表成员存储的是对象的引用，而不是对象本身
'''